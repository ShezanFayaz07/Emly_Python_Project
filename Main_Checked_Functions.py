def validateGuess(guess):
    if len(guess) != 1 or not guess.isalpha():
        return False
    return True
#-----------------------------------------------------------------------------------------------------------------------
#///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#-----------------------------------------------------------------------------------------------------------------------
def isRepeatedGuess(guess, guessed_letters, Wrong_letters):
    for letter in guessed_letters.union(Wrong_letters):
        if guess == letter:
            return True
    return False
