def displayWordProgress(word, guessed_letters, attempts_left):
    progress = ""
    for letter in word:
        if letter in guessed_letters:
            progress += " " + letter + " "
        else:
            progress += " x "
    print(f"\033[1;30;47m{progress.strip()}  {Red}|-=-=-> Attempts Left: {attempts_left}\033[0m\n")
